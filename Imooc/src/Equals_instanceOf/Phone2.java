package Equals_instanceOf;

public class Phone2 {
	private float screen;
	private float CPU;
	private float RAM;
	
	public Phone2(){
		screen=5.0f;
		CPU=1.2f;
		RAM=2.0f;
	}

	public float getScreen() {
		return screen;
	}

	public void setScreen(float screen) {
		this.screen = screen;
	}

	public float getCPU() {
		return CPU;
	}

	public void setCPU(float cPU) {
		CPU = cPU;
	}

	public float getRAM() {
		return RAM;
	}

	public void setRAM(float rAM) {
		RAM = rAM;
	}
	
}
